import streamlit as st
import pandas as pd
from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score
import matplotlib.pyplot as plt
import io

# -----------------------------------
# Page Configuration
# -----------------------------------
st.set_page_config(
    page_title="Dynamic House Price Prediction",
    page_icon="🏠",
    layout="wide" # Changed to wide layout for better visualization
)

st.title("🏠 Dynamic House Price Prediction")
st.write("Upload your data, select features, and predict prices using Linear Regression.")

# -----------------------------------
# Sidebar for User Inputs
# -----------------------------------
st.sidebar.header("Upload Data & Select Features")

uploaded_file = st.sidebar.file_uploader("Upload your CSV file", type=["csv"])

df = None # Initialize df to None

if uploaded_file is not None:
    try:
        df = pd.read_csv("https://github.com/sachin365123/linear_regression_deployment/blob/main/houseprice.csv")
    except Exception as e:
        st.error(f"Error loading file: {e}. Please ensure it's a valid CSV.")
        st.stop()
else:
    # Provide a sample dataset if no file is uploaded
    st.info("No CSV uploaded. A sample dataset ('houseprice.csv') will be used.")
    try:
        # Ensure 'houseprice.csv' exists for this to work, otherwise provide a dummy
        df = pd.read_csv("houseprice.csv")
    except FileNotFoundError:
        st.warning("Sample 'houseprice.csv' not found. Please upload your own CSV file to proceed.")
        st.stop()

# -----------------------------------
# Data Preview
# -----------------------------------
if df is not None:
    st.subheader("Dataset Preview")
    st.dataframe(df.head())

    # -----------------------------------
    # Feature and Target Selection
    # -----------------------------------
    st.sidebar.subheader("Feature and Target Selection")
    numeric_cols = df.select_dtypes(include=['number']).columns.tolist()

    if len(numeric_cols) < 2:
        st.sidebar.error("Your dataset must contain at least two numeric columns for a linear regression.")
        st.stop()

    # Default to 'area' and 'price' if they exist, otherwise pick first two numeric cols
    default_feature_index = numeric_cols.index('area') if 'area' in numeric_cols else 0
    feature_column = st.sidebar.selectbox(
        "Select Feature Column (X)",
        numeric_cols,
        index=default_feature_index
    )

    target_options = [col for col in numeric_cols if col != feature_column]
    if not target_options:
        st.sidebar.error("Cannot select target column. Please ensure at least two distinct numeric columns are available after feature selection.")
        st.stop()

    default_target_index = target_options.index('price') if 'price' in target_options else 0
    target_column = st.sidebar.selectbox(
        "Select Target Column (y)",
        target_options,
        index=default_target_index
    )

    # Drop rows with NaN in selected columns
    df_cleaned = df[[feature_column, target_column]].dropna()

    if df_cleaned.empty:
        st.error("No valid data points after removing rows with missing values in selected columns. Please check your data.")
        st.stop()

    X = df_cleaned[[feature_column]]
    y = df_cleaned[target_column]

    # -----------------------------------
    # Train Model
    # -----------------------------------
    model = LinearRegression()
    model.fit(X, y)
    y_pred = model.predict(X)

    # -----------------------------------
    # Prediction User Input
    # -----------------------------------
    st.sidebar.subheader("Make a Prediction")
    min_val = float(X.min().values[0])
    max_val = float(X.max().values[0])
    avg_val = float(X.mean().values[0])
    step_val = max(1.0, float((max_val - min_val) / 100)) # Ensure step is at least 1

    input_value = st.sidebar.number_input(
        f"Enter {feature_column}",
        min_value=min_val,
        max_value=max_val,
        value=avg_val,
        step=step_val
    )

    if st.sidebar.button("Predict Price"):
        prediction = model.predict([[input_value]])
        st.sidebar.success(f"Predicted {target_column}: ₹ {prediction[0]:,.2f}")

    # -----------------------------------
    # Model Performance and Details
    # -----------------------------------
    st.subheader("Model Performance and Details")
    r2 = r2_score(y, y_pred)
    st.write(f"**R-squared (R²):** {r2:.4f}")
    st.write(f"**Coefficient ({feature_column}):** {model.coef_[0]:.4f}")
    st.write(f"**Intercept:** {model.intercept_:.4f}")

    # -----------------------------------
    # Visualization
    # -----------------------------------
    st.subheader("Regression Plot")
    fig, ax = plt.subplots(figsize=(10, 6))
    ax.scatter(df_cleaned[feature_column], df_cleaned[target_column], color='blue', label='Actual Data', alpha=0.6)
    ax.plot(df_cleaned[feature_column], y_pred, color='red', label='Regression Line', linewidth=2)
    ax.set_xlabel(feature_column)
    ax.set_ylabel(target_column)
    ax.set_title(f"Linear Regression: {feature_column} vs {target_column}")
    ax.legend()
    ax.grid(True, linestyle='--', alpha=0.7)
    st.pyplot(fig)
else:
    st.info("Please upload a CSV file in the sidebar to start.")

